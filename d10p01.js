let errorScore = 0;

const isLineCorrupted = (line) => {
    let lineChars = [];
    for (let charIdx = 0, charsCount = line.length; charIdx < charsCount; charIdx++) {
        const char = line[charIdx];
        switch (char) {
            case ")":
                if (lineChars[lineChars.length - 1] !== "(") {
                    errorScore += 3;
                    return true;
                }
                lineChars.pop();
                break;
            case "]":
                if (lineChars[lineChars.length - 1] !== "[") {
                    errorScore += 57;
                    return true;
                }
                lineChars.pop();
                break;
            case "}":
                if (lineChars[lineChars.length - 1] !== "{") {
                    errorScore += 1197;
                    return true;
                }
                lineChars.pop();
                break;
            case ">":
                if (lineChars[lineChars.length - 1] !== "<") {
                    errorScore += 25137;
                    return true;
                }
                lineChars.pop();
                break;
            default:
                lineChars.push(char);
        }
    }
};

